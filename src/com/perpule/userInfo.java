package com.perpule;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement 
public class userInfo {
	
	private String name;
	private int age;
	private int id;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public int getAge() {
		return age;
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	
}
